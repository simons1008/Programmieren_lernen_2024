# Das ist ein Kommentar
# Rechnen
nachricht = "Hallo Welt "
print(3 * nachricht)
ergebnis = 3 * 4
print(ergebnis/5)

# Umwandeln
zahl = 3.14
print(zahl)
print(str(zahl))
zeichenkette = "3.14 "
print(zeichenkette)
print(3 * zeichenkette)
print(3 * float(zeichenkette))

# Variablen ausgeben
print("nachricht =", nachricht)
print("ergebnis =", ergebnis)
print("zahl =", zahl)
print("zeichenkette =", zeichenkette)
