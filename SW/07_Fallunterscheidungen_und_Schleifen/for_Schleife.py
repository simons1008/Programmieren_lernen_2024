# Eine for-Schleife ausführen

# Liste initialisieren
vornamen = ["Axel", "Elke", "Martin"]

# Solange es ein Element in vornamen gibt: Durchlaufe die Schleife
for element in vornamen:
    # Gib das aktuelle Element aus
    print(element)

# Diese Anweisung gehört nicht mehr zur Schleife
print("nach der for-Schleife")
